static const U8 Rcon[11] = {
    0x00, 0x01, 0x02, 0x04,
    0x08, 0x10, 0x20, 0x40,
    0x80, 0x1B, 0x36};

static void KeyExpansion(U8 *RoundKey, const U8 *Key)
{
    unsigned i, j;
    U8 temp[4];

    for (i = 0; i < Nk; ++i)
    {
        RoundKey[i * 4 + 0] = Key[i * 4 + 0];
        RoundKey[i * 4 + 1] = Key[i * 4 + 1];
        RoundKey[i * 4 + 2] = Key[i * 4 + 2];
        RoundKey[i * 4 + 3] = Key[i * 4 + 3];
    }

    for (i = Nk; i < Nb * (Nr + 1); ++i)
    {
        for (j = 0; j < 4; ++j)
        {
            temp[j] = RoundKey[(i - 1) * 4 + j];
        }
        if (i % Nk == 0)
        {
            U8 t = temp[0];
            temp[0] = sbox[temp[1]];
            temp[1] = sbox[temp[2]];
            temp[2] = sbox[temp[3]];
            temp[3] = sbox[t];
            temp[0] ^= Rcon[i / Nk];
        }
        for (j = 0; j < 4; ++j)
        {
            RoundKey[i * 4 + j] = RoundKey[(i - Nk) * 4 + j] ^ temp[j];
        }
    }
}

static void AddRoundKey(U8 round, U8 *state, const U8 *RoundKey)
{
    for (U8 i = 0; i < 16; ++i)
    {
        state[i] ^= RoundKey[round * Nb * 4 + i];
    }
}

static void SubBytes(U8 *state)
{
    for (U8 i = 0; i < 16; ++i)
    {
        state[i] = sbox[state[i]];
    }
}

static void InvSubBytes(U8 *state)
{
    for (U8 i = 0; i < 16; ++i)
    {
        state[i] = rsbox[state[i]];
    }
}

static void ShiftRows(U8 *state)
{
    U8 tmp;

    tmp = state[1];
    state[1] = state[5];
    state[5] = state[9];
    state[9] = state[13];
    state[13] = tmp;

    tmp = state[2];
    state[2] = state[10];
    state[10] = tmp;
    tmp = state[6];
    state[6] = state[14];
    state[14] = tmp;

    tmp = state[3];
    state[3] = state[15];
    state[15] = state[11];
    state[11] = state[7];
    state[7] = tmp;
}

static void InvShiftRows(U8 *state)
{
    U8 tmp;

    tmp = state[13];
    state[13] = state[9];
    state[9] = state[5];
    state[5] = state[1];
    state[1] = tmp;

    tmp = state[2];
    state[2] = state[10];
    state[10] = tmp;
    tmp = state[6];
    state[6] = state[14];
    state[14] = tmp;

    tmp = state[3];
    state[3] = state[7];
    state[7] = state[11];
    state[11] = state[15];
    state[15] = tmp;
}

static U8 xtime(U8 x)
{
    return (x << 1) ^ ((x >> 7) * 0x1b);
}

static void MixColumns(U8 *state)
{
    U8 Tmp, Tm, t;
    for (U8 i = 0; i < 4; ++i)
    {
        t = state[i * 4 + 0];
        Tmp = state[i * 4 + 0] ^ state[i * 4 + 1] ^ state[i * 4 + 2] ^ state[i * 4 + 3];
        Tm = state[i * 4 + 0] ^ state[i * 4 + 1];
        Tm = xtime(Tm);
        state[i * 4 + 0] ^= Tm ^ Tmp;
        Tm = state[i * 4 + 1] ^ state[i * 4 + 2];
        Tm = xtime(Tm);
        state[i * 4 + 1] ^= Tm ^ Tmp;
        Tm = state[i * 4 + 2] ^ state[i * 4 + 3];
        Tm = xtime(Tm);
        state[i * 4 + 2] ^= Tm ^ Tmp;
        Tm = state[i * 4 + 3] ^ t;
        Tm = xtime(Tm);
        state[i * 4 + 3] ^= Tm ^ Tmp;
    }
}

static U8 mul(U8 x, U8 y)
{
    return (((y & 1) * x) ^
            ((y >> 1 & 1) * xtime(x)) ^
            ((y >> 2 & 1) * xtime(xtime(x))) ^
            ((y >> 3 & 1) * xtime(xtime(xtime(x)))) ^
            ((y >> 4 & 1) * xtime(xtime(xtime(xtime(x))))));
}

static void InvMixColumns(U8 *state)
{
    U8 a, b, c, d;
    for (U8 i = 0; i < 4; ++i)
    {
        a = state[i * 4 + 0];
        b = state[i * 4 + 1];
        c = state[i * 4 + 2];
        d = state[i * 4 + 3];

        state[i * 4 + 0] = mul(a, 0x0e) ^ mul(b, 0x0b) ^ mul(c, 0x0d) ^ mul(d, 0x09);
        state[i * 4 + 1] = mul(a, 0x09) ^ mul(b, 0x0e) ^ mul(c, 0x0b) ^ mul(d, 0x0d);
        state[i * 4 + 2] = mul(a, 0x0d) ^ mul(b, 0x09) ^ mul(c, 0x0e) ^ mul(d, 0x0b);
        state[i * 4 + 3] = mul(a, 0x0b) ^ mul(b, 0x0d) ^ mul(c, 0x09) ^ mul(d, 0x0e);
    }
}

static void Cipher(U8 *state, const U8 *RoundKey)
{
    AddRoundKey(0, state, RoundKey);
    for (U8 round = 1; round < Nr; ++round)
    {
        SubBytes(state);
        ShiftRows(state);
        MixColumns(state);
        AddRoundKey(round, state, RoundKey);
    }
    SubBytes(state);
    ShiftRows(state);
    AddRoundKey(Nr, state, RoundKey);
}

static void InvCipher(U8 *state, const U8 *RoundKey)
{
    AddRoundKey(Nr, state, RoundKey);
    for (U8 round = Nr - 1; round > 0; --round)
    {
        InvShiftRows(state);
        InvSubBytes(state);
        AddRoundKey(round, state, RoundKey);
        InvMixColumns(state);
    }
    InvShiftRows(state);
    InvSubBytes(state);
    AddRoundKey(0, state, RoundKey);
}

void AES_init_ctx(AES_ctx *ctx, const U8 *key)
{
    KeyExpansion(ctx->RoundKey, key);
}

void AES_ECB_encrypt(const AES_ctx *ctx, U8 *buf)
{
    Cipher(buf, ctx->RoundKey);
}

void AES_ECB_decrypt(const AES_ctx *ctx, U8 *buf)
{
    InvCipher(buf, ctx->RoundKey);
}

// AES wrapper with ECB + PKCS7 (simulate your aes128 function)
U8 *aes128(const U8 *data, U8 data_len, const U8 *key, U8 key_len, U8 operation)
{
    if (!data || !key || key_len != BASE128)
        return NULL;

    // Calculate padded length (multiple of 16)
    U8 padded_len = ((data_len / BASE128) + 1) * BASE128;

    U8 *buffer = malloc(padded_len);
    if (!buffer)
        return NULL;

    memset(buffer, 0, padded_len);
    memcpy(buffer, data, data_len);

    AES_ctx ctx;
    AES_init_ctx(&ctx, key);

    if (operation == ENCRYPT)
    {
        // PKCS#7 padding
        U8 pad = padded_len - data_len;
        for (U8 i = data_len; i < padded_len; i++)
            buffer[i] = pad;

        for (U8 i = 0; i < padded_len; i += BASE128)
            AES_ECB_encrypt(&ctx, buffer + i);
    }
    else if (operation == DECRYPT)
    {
        for (U8 i = 0; i < padded_len; i += BASE128)
            AES_ECB_decrypt(&ctx, buffer + i);

        // Remove PKCS#7 padding
        U8 pad = buffer[padded_len - 1];
        if (pad <= BASE128)
        {
            for (U8 i = 0; i < pad; i++)
            {
                if (buffer[padded_len - 1 - i] != pad)
                    return buffer; // Invalid padding; return raw data
                buffer[padded_len - 1 - i] = 0;
            }
        }
    }

    return buffer;
}
